export type TranslationKey = keyof typeof CATALOG;

export function translate(lang: Language, key: TranslationKey, vars?: Record<string, string | number>): string {
  const entry: Partial<Record<Language, string>> | undefined = CATALOG[key];
  // A missing key can only happen via an invalid/stale key at runtime (every
  // real catalog entry is compile-time checked to cover all languages) --
  // fall back to the raw key itself so a typo is visible in dev rather than
  // crashing the render.
  let text: string = entry?.[lang] ?? entry?.en ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.split(`{${name}}`).join(String(value));
    }
  }
  return text;
}
